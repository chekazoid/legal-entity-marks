$fail = 0;
function t($desc, $text, $entity, $settings, $want, $want_form = null) {
    global $fail;
    $hit = LEM_Scanner::match_entity($text, $entity, $settings);
    $got = $hit !== null;
    $ok  = $got === $want;
    if ($ok && $want_form !== null) {
        $ok = ($hit['matched_as'] === $want_form);
    }
    if (!$ok) { $fail++; }
    printf("%s  %-46s %s%s\n", $ok ? 'OK  ' : 'ФЕЙЛ', mb_substr($desc, 0, 46),
        $got ? 'найдено «' . $hit['matched_as'] . '»' : 'не найдено',
        ($want_form !== null && $got) ? " (ждали «" . $want_form . "»)" : '');
}

